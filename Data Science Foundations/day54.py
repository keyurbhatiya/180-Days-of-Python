# Data Visualization with Matplotlib

''''
Matplotlib is a library for creating charts and graphs in Python. It provides a high-level API for creating plots and visualizations.
'''

# Example

import matplotlib.pyplot as plt

x = [1, 2, 3, 4, 5]
y = [2, 4, 6, 8, 10]

plt.plot(x, y)
plt.show()

# Output
'''
[1, 2, 3, 4, 5]
[2, 4, 6, 8, 10]
'''