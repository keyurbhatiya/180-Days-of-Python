# Day 4: Conditional Statements (if, elif, else)


# Conaditional Statements

'''
if
elif
else

'''

# if Statement
print("if Statement")
x = 10
if x > 5:
    print("x is greater than 5")

# elif Statement
print("elif Statement")
x = 10
if x > 5:
    print("x is greater than 5")
elif x < 5:
    print("x is less than 5")

# else Statement
print("else Statement")
x = 10
if x > 5:
    print("x is greater than 5")
elif x < 5:
    print("x is less than 5")
else:
    print("x is equal to 5")