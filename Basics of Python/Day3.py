# Arithmetic Operators

'''
+	Addition
-	Subtraction
*	Multiplication
/	Division
%	Modulus
**	Exponentiation
//	Floor division
'''
print("Arithmetic Operators")
print(10+5)
print(10-5)
print(10*5)
print(10/5)
print(10%5)
print(10**5)
print(10//5)


# Logical 
'''

and
or
not

'''
print("Logical Operators")
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)


# Comparison

'''
==	Equal
!=	Not equal
>	Greater than
<	Less than
>=	Greater than or equal to
<=	Less than or equal to
'''
print("Comparison Operators")
print(10==10)
print(10!=10)
print(10>10)
print(10<10)
print(10>=10)
print(10<=10)


# Bitwise

'''
&	Bitwise AND
|	Bitwise OR
^	Bitwise XOR
~	Bitwise NOT
<<	Bitwise left shift
>>	Bitwise right shift
'''
print("Bitwise Operators")
print(10&10)
print(10|10)
print(10^10)
print(~10)
print(10<<10)
print(10>>10)

